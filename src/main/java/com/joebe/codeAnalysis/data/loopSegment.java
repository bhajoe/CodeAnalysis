package com.joebe.codeAnalysis.data;

/**
 *
 * @author bayup
 */
public class loopSegment {
    private String loopBody;
    private int start;
    private int end;

    public String getLoopBody() {
        return loopBody;
    }

    public void setLoopBody(String loopBody) {
        this.loopBody = loopBody;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
    
    
}
